void FillImage(int rows, int columns)
{
    if (pic[rows,columns] == 0) 
    {
       pic[rows,columns] = 1;
       FillImage(rows-1,columns);
       FillImage(rows,columns-1);
       FillImage(rows+1,columns);
       FillImage(rows,columns+1); 
    }
} 

